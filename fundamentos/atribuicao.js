const a = 7;
let b = 3;


b += a; // += acrescenta            (b = b + a)
console.log(b);


b -= a; // subtração                       (b = b - a)
console.log(b);

b *= 2; //multiplicativa                  (b = b * a)
console.log(b)

b /= 2//divisao                          (b = b/2)
console.log(b);


b %= 2 //  modulo (resto da dividao)                 (b = b % 2)
console.log(b);